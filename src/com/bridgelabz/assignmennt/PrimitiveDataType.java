package com.bridgelabz.assignmennt;

import java.security.KeyStore;

public class PrimitiveDataType {
    public static void main(String[] args) {
        byte a = 120;
        short b = 127;
        int c = 30;
        long d = 40;
        float e = 1;
        double f = 32;
        char g = 'r';
        boolean p = true;


        System.out.println("the byte primitive data type value of a is " + a);
        System.out.println("the short primitive data type value of b is " + b);
        System.out.println("the int primitive data type value of c is " + c);
        System.out.println("the long primitive data type value of d is " + d);
        System.out.println("the float primitive data type value of e is " + e);
        System.out.println("the double primitive data type value of f is " + f);
        System.out.println("the char primitive data type value of g is " + g);
        System.out.println("the boolean primitive data type value of p is " + p);
    }
}
